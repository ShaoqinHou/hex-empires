a('## Purpose');
a('');
a('Combat in Civ VII is the resolution engine for all military conflict between units. It retains Civ V’s one-unit-per-tile model but adds a directional battlefront system (flanking by position) and decomposes city capture into district-by-district siege. Commanders (covered separately in `systems/commanders.md`) provide the army-aggregation layer — this doc covers the combat resolution engine that commanders interact with but do not own.');
