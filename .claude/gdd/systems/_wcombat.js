a('## Triggers');
a('');
a('- On action **ATTACK** (player dispatches a unit to attack an adjacent or ranged-eligible tile) — combat resolution fires immediately; attacker and defender both receive or deal damage; attacker expends remaining movement points.');
a('- On action **BOMBARD** (ranged or siege unit fires on an in-range target) — attacker uses `rangedStrength` or `bombardStrength` depending on target type; defender does NOT return fire.');
a('- On action **FORTIFY** (unit uses its Fortify action) — unit becomes immobile; gains +5 CS when defending; begins healing on subsequent turns.');
a('- On **END_TURN** (player ends turn) — healing ticks for all units that did not move or attack this turn; `hp` increments by territory-dependent heal amount.');
a('- On condition **district HP reaches 0** — district walls are destroyed; fortified district tile converts to unfortified; occupying enemy unit may claim the tile.');
a('- On condition **all fortified districts destroyed AND unit moves onto City Center tile** — settlement is captured; enters unrest period; production temporarily blocked.');
